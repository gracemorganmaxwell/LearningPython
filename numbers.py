# In Python there are three types of numbers, integers, floats (decimals) and complex numbers
# But really only need to worry about the first two. |
# Then there's the standard arithmetic operations

print(10 + 3)  # Addition
print(10 - 3)  # Subtraction
print(10 * 3)  # Multiplication
print(10 / 3)  # Pure division
print(10 // 3)  # Whole number division
print(10 % 3)  # Modulus: the remainder of the division
print(10 ** 3)  # Exponent, left to the power of the right

