# no escape sequences
course = "python programming"
print(course)

# \"
course_one = "python \"fundamentals"
print(course_one)

# \'
course_two = "python \'fundamentals"
print(course_two)

# \\
course_three = "python \\fundamentals"
print(course_three)

# \n
course_four = "python \nfundamentals"
print(course_four)